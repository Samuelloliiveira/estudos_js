// LOGICAL OPERATORS

/* - 2 valores booleanos, quando verificados,
resultará em verdadeiro ou falso */

let pao = true
let queijo = true

// AND &&
console.log(pao && queijo) //tem pão e(and) queijo

//OR ||
console.log(pao || queijo) //tem pão ou(or) queijo

//NOT !
console.log(!pao); // não tem pão
let cafe = false //falando que não tem café
console.log(!cafe); //negando e afirmando que tem sim café